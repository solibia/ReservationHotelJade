    private void btnRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercherActionPerformed
        // TODO add your handling code here: 
        if (EstVide()) {
            infoBox("Veuillez remplir tous les champs");
        }else{
                    btnRechercher.setEnabled(false);
        BesoinsClient besoinClient = new BesoinsClient();
        //besoinClient.setNom(txtFieldNomClient.getText());
        besoinClient.setLocalite(txtFieldLocalite.getText());
        besoinClient.setTypeChambre("VIP");
        //besoinClient.setPrixMax((int) spinPrixMax.getValue());
        String pattern = "dd/MM/yyyy";
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat(pattern);
        String date1 = simpleDateFormat.format(datDebut.getValue());
        datDebut.setText(date1);
        String date2 = simpleDateFormat.format(datFin.getValue());
        datFin.setText(date2);
        //System.out.println("Date1 = "+date1);
        //System.out.println("Date2 = "+date2);
        besoinClient.setDatArrive((Date) datDebut.getValue());
        besoinClient.setDatDepart((Date) datFin.getValue());
        besoinClient.setPrixMax((int) spinPrixMax.getValue());
        besoinClient.setPrixMin((int) spinPrixMin.getValue());
        afficheBesoinClient(besoinClient);
        clientAgent.setUserRequest(besoinClient);
        }

    }//GEN-LAST:event_btnRechercherActionPerformed

