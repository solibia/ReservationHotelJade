    boolean EstVide() {
        boolean rep = true;
        if (txtFieldLocalite.toString().isEmpty()
                || datDebut.getValue()==null
                || datFin.getValue()==null
                || (int) spinPrixMax.getValue() <= 0 
                || (int) spinPrixMin.getValue() <= 0) {
            rep = true;
        }else{
            rep = false;
        }
        return rep;
    }

